const arr = input.split('\n').map(el => el.split(''))
const points = {
    ')': 3,
    ']': 57,
    '}': 1197,
    '>': 25137
}
const closing = Object.keys(points)
const checkSame = (a, b) => {
    return (a === '[' && b === ']') || (a === '{' && b === '}') || (a === '<' && b === '>') || (a === '(' && b === ')')
}

const getWrongChunk = (line, index = 1) => {
    if (index >= line.length) {
        return null
    }
    if (closing.includes(line[index]) && !checkSame(line[index - 1], line[index])) {
        return line[index]
    }
    if (closing.includes(line[index]) && checkSame(line[index - 1], line[index])) {
        line.splice(index - 1,2)
        console.log(line.join(''));
        return getWrongChunk(line, index - 1)
    }
    return getWrongChunk(line, index + 1)
}
const chunks = arr.map(el => getWrongChunk(el))
const res = chunks
    .filter(el => el !== null)
    .reduce((acc, cur) => acc + points[cur], 0)

